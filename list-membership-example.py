# List Membership Example

# Create a list with a few elements in it
fruits = ['kiwi', 'mango', 'apple']

# Print the original list
print(f'Original list: {fruits}')

# Add more elements using the append ( ) method
fruits.append('banana')
fruits.append('lime')

# Print the updated list of fruits
print(f'Updated list: {fruits}')